<template>
  <div>
    <p>Mutations</p>
    <button class="btn btn-success" @click="increaseCounter">+ Artt覺r</button>
    <button class="btn btn-danger" @click="decreaseCounter">- Azalt</button>
    <hr />
    <p>Actions</p>
    <button class="btn btn-success" @click="increment">+ Artt覺r</button>
    <button class="btn btn-danger" @click="decrement">- Azalt</button>
    <hr />
    <p>Actions ile Async Artt覺r (2 saniye sonra)</p>
    <button class="btn btn-success" @click="incAsync">+ Async Artt覺r</button>
  </div>
</template>

<script>
import { mapMutations, mapActions } from 'vuex';
export default {
  name: "Counter",
  methods: {
      ...mapMutations([
          'increaseCounter',
          'decreaseCounter',
      ]),
      ...mapActions([
          'increment',
          'decrement',
          'incAsync'
      ])
  }
};
</script>

